    private void aksiLogin(){
        viduser = txtUser.getText();
        vpass = txtPassword.getText();
        try{
            _Cnn = null;
            _Cnn = getCnn.getConnection();
            String sql = "SELECT * from tbuser where id_user='"+viduser+"' and " + "pass='"+vpass+"' ";
            Statement stat = (Statement) _Cnn.createStatement();
            ResultSet res = stat.executeQuery(sql);
            if(res.first()){
                panelLogin.setVisible(false);
                panelKiri.setEnabled(true);
                panelDekstop.setEnabled(true);
                mnFile.setEnabled(true);
                mnMaster.setEnabled(true);
                mnMahasiswa.setEnabled(true);
                btnCetak.setEnabled(true);
                btnJurusan.setEnabled(true);
                btnMhs.setEnabled(true);
                btnProdi.setEnabled(true);
            }else {
                JOptionPane.showMessageDialog(this, "ID. User dan Password salah !", 
                        "Informasi", JOptionPane.INFORMATION_MESSAGE);
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, "Error method aksiLogin() : "+ex,
                    "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }
