    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        if(txtUser.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Username harus diisi !","Informasi", JOptionPane.INFORMATION_MESSAGE);
        }else if(txtPassword.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Password harus diisi !","Informasi", JOptionPane.INFORMATION_MESSAGE);
        }else if(txtUser.getText().equals("") && txtPassword.getText().equals("") ){
            JOptionPane.showMessageDialog(this, "Username dan Password harus diisi !","Informasi", JOptionPane.INFORMATION_MESSAGE);
        }else{
            aksiLogin();
        }
    }//GEN-LAST:event_btnLoginActionPerformed

